/* ================================  Find even & odd number using for loop & if else ============================================ */ 


for(let i=2; i <= 20; i++){
    if(i % 2 == 0){
        console.log('Even number :- ' + i)
    }
    else if(i%2 != 0){
        console.log('Odd number :- ' + i);
    }
}

